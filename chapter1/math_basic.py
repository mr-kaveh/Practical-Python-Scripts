import math

# Example 1: Calculating the square root of a number
number = 25
square_root = math.sqrt(number)
print(f"The square root of {number} is {square_root}")

# Example 2: Calculating the factorial of a number
factorial_number = 5
factorial_result = math.factorial(factorial_number)
print(f"The factorial of {factorial_number} is {factorial_result}")

# Example 3: Calculating trigonometric functions
angle_in_degrees = 45
angle_in_radians = math.radians(angle_in_degrees)
sin_value = math.sin(angle_in_radians)
cos_value = math.cos(angle_in_radians)
print(f"sin({angle_in_degrees} degrees) = {sin_value}")
print(f"cos({angle_in_degrees} degrees) = {cos_value}")

