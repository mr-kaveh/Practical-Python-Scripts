# Define a function to calculate the square of a number
def square(number):
    return number ** 2

# Test the function by calculating squares of numbers
number1 = 5
number2 = 8
number3 = 12

result1 = square(number1)
result2 = square(number2)
result3 = square(number3)

# Print the results
print(f"The square of {number1} is {result1}")
print(f"The square of {number2} is {result2}")
print(f"The square of {number3} is {result3}")
